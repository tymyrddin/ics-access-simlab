# DMZ zone
# ---------------------------------------------------------------------------

def generate_dmz_compose(config: dict, output_path: Path) -> dict:
    """Generate zones/dmz/docker-compose.yml.

    Iterates dmz_zone.devices. Devices with an enterprise_ip are dual-homed
    on both ics_dmz and ics_enterprise (simulates contractor pivot path).
    """
    dmz_net = _net(config, "dmz")
    ent_net = _net(config, "enterprise")
    base_dir = output_path.parent
    services = {}
    networks_used = {dmz_net}

    for dev in config.get("dmz_zone", {}).get("devices", []):
        impl = dev["implementation"]
        _check_impl(impl)
        svc_name = dev["name"].replace("_", "-")

        networks = {dmz_net: {"ipv4_address": dev["ip"]}}
        if "enterprise_ip" in dev:
            networks[ent_net] = {"ipv4_address": dev["enterprise_ip"]}
            networks_used.add(ent_net)

        svc = {
            "build": {"context": _rel(COMPONENT_DIRS[impl], base_dir)},
            "container_name": dev["name"],
            "hostname": dev.get("hostname", dev["name"]),
            "restart": "unless-stopped",
            "networks": networks,
        }
        if dev.get("env"):
            svc["environment"] = dev["env"]

        if dev.get("syslog_logging"):
            svc["logging"] = {
                "driver": "syslog",
                "options": {
                    "syslog-address": "udp://10.10.5.32:514",
                    "tag": dev.get("hostname", dev["name"]),
                },
            }
            svc.setdefault("depends_on", []).append("syslog-relay")

        services[svc_name] = svc

    return {
        "services": services,
        "networks": {n: _external_net(n) for n in networks_used},
    }


# ---------------------------------------------------------------------------
# Inter-zone firewall (DOCKER-USER chain, applied on the host after zones up)
# ---------------------------------------------------------------------------

def generate_firewall_sh(config: dict) -> str:
    """Generate infrastructure/firewall.sh from orchestrator/firewall-rules.txt.

    The rules file contains iptables commands with {placeholder} addresses.
    Placeholders are resolved from ctf-config.yaml and substituted here.
    Edit firewall-rules.txt to change policy; run 'make generate' afterwards.
    """
    addrs = {
        "internet":      config["networks"]["internet"]["subnet"],
        "enterprise":    config["networks"]["enterprise"]["subnet"],
        "operational":   config["networks"]["operational"]["subnet"],
        "control":       config["networks"]["control"]["subnet"],
        "wan":           config["networks"]["wan"]["subnet"],
        "dmz":           config["networks"]["dmz"]["subnet"],
        "historian":     config["operational_zone"]["historian"]["ip"],
        "scada":         config["operational_zone"]["scada_server"]["ip"],
        "eng_ws":        config["operational_zone"]["engineering_workstation"]["ip"],
        # admin_home internet IP — used for VPN ACCEPT rule before internet→enterprise DROP.
        # Falls back to 0.0.0.0/32 (no-op) when internet_zone is absent from config.
        "admin_home_ip": (
            config.get("internet_zone", {})
                  .get("admin_home", {})
                  .get("internet_ip", "0.0.0.0/32")
        ),
        # ssh-bastion DMZ IP — used for contractor pivot ACCEPT rule before dmz→enterprise DROP.
        # Falls back to 0.0.0.0/32 (no-op) when dmz_zone is absent from config.
        "ssh_bastion": next(
            (d["ip"] for d in config.get("dmz_zone", {}).get("devices", [])
             if d.get("implementation") == "ssh-bastion-vuln"),
            "0.0.0.0/32"
        ),
    }
    # Strip comment header lines from the rules file — the generated script
    # has its own header; the txt file comments are for human editors only.
    raw = FIREWALL_RULES.read_text()
    body = "\n".join(
        line for line in raw.splitlines() if not line.startswith("#")
    ).strip()
    rules = body.format_map(addrs)
    return (
        "#!/usr/bin/env bash\n"
        "# Inter-zone firewall rules — DOCKER-USER chain\n"
        "# Generated by orchestrator/generate.py — do not edit directly.\n"
        "# Edit orchestrator/firewall-rules.txt to change policy.\n"
        "#\n"
        "# Requires root (iptables). Applied after all zone stacks are up.\n"
        "set -euo pipefail\n"
        "\n"
        'if [ "$EUID" -ne 0 ]; then\n'
        '    echo "[firewall] Error: iptables rules require root. Run as root or with sudo." >&2\n'
        "    exit 1\n"
        "fi\n"
        "\n"
        "iptables -F DOCKER-USER\n"
        "\n"
        + rules
        + '\necho "[firewall] Inter-zone rules applied."\n'
    )




# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _check_impl(name: str) -> None:
    if name not in COMPONENT_DIRS:
        raise ValueError(
            f"Unknown implementation/vendor: {name!r}. "
            f"Known: {list(COMPONENT_DIRS)}"
        )


def write_compose(path: Path, compose_dict: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        yaml.dump(compose_dict, f, sort_keys=False, default_flow_style=False)
    logging.info(f"Wrote: {path}")


def write_script(path: Path, content: str) -> None:
    path.write_text(content)
    path.chmod(0o755)
    logging.info(f"Wrote: {path}")


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)
    logging.info(f"Wrote: {path}")


