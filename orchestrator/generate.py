def _net(config: dict, key: str) -> str:
    return config["networks"][key]["docker_name"]


def _subnet(config: dict, key: str) -> str:
    return config["networks"][key]["subnet"]


def _external_net(name: str) -> dict:
    return {"external": True, "name": name}


# ---------------------------------------------------------------------------
# Shared networks compose (must start before zone stacks)
# ---------------------------------------------------------------------------

def generate_networks_compose(config: dict) -> dict:
    net_names = [n["docker_name"] for n in config["networks"].values()]
    networks = {}
    for net_cfg in config["networks"].values():
        name = net_cfg["docker_name"]
        networks[name] = {
            "driver": "bridge",
            "name": name,
            "ipam": {"config": [{"subnet": net_cfg["subnet"]}]},
        }
    return {
        "services": {
            "network-init": {
                "image": "alpine:3.19",
                "command": ["/bin/true"],
                "restart": "no",
                "networks": net_names,
            }
        },
        "networks": networks,
    }


